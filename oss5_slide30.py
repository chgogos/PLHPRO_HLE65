# Επανάληψη μέσα στην επανάληψη (Φωλιασμένοι βρόχοι)
# Πίνακας Προπαίδειας

for i in range(1, 11):
    for j in range(1, 11):
        # Βάζω ένα στηλοθέτη για να τυπώσει μετά στην 8η στήλη
        # και το end='' για να παραμείνει σε αυτή τη θέση να μην αλλάξει γραμμή
        print(i * j, "\t", end="")
print("\n")
