# H Continue χρησιμοποιείται αν θέλετε να πάτε στην επόμενη επανάληψη
# αλλά χωρίς να εκτελεστούν κάποιες εντολές μέσα στο σώμα της επανάληψης 

i=0
while i<100:
    i +=5
    if i > 20:
        continue
    print(i)
print('\n--- Τέλος Επανάληψης ---')
print(i)    
